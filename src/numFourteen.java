public class numFourteen {
    public static void main (String[] args){
        int len = 5;
        int initialValue = 8;
        int[]arr = new int[len];
        for (int a = 0; a<len; a++){
            arr[a] = initialValue;
            System.out.println("[" + a + "]" + arr[a] + " ");
        }
    }
}
